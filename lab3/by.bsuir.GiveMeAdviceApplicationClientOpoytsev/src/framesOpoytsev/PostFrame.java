    public void init() throws RemoteException, NotBoundException, SQLException {
        Registry registry = LocateRegistry.getRegistry("localhost", 1112);
        MyRemote remoteObject = (MyRemote) registry
                .lookup("RemoteObject");
        dtm = remoteObject.getPost(ID);
        title.setText(dtm.getValueAt(0, 1).toString());
        text.setText(dtm.getValueAt(0, 2).toString());
         if (dtm.getValueAt(0, 3).toString().equals("")
                && dtm.getValueAt(0, 4).toString().equals("")
                && dtm.getValueAt(0, 5).toString().equals("")
                && dtm.getValueAt(0, 6).toString().equals("")) {
            tags.setText("");
        } else {
            tags.setText(dtm.getValueAt(0, 3).toString() + " "
                    + dtm.getValueAt(0, 4).toString() + " "
                    + dtm.getValueAt(0, 5).toString() + " "
                    + dtm.getValueAt(0, 6).toString());
        }
        datetime.setText(dtm.getValueAt(0, 8).toString());
        refresh();
        
    }

    public void refresh() throws RemoteException, NotBoundException, SQLException {
        DefaultTableModel dtm = new DefaultTableModel();
        Registry registry = LocateRegistry.getRegistry("localhost", 1112);
        MyRemote remoteObject = (MyRemote) registry
                .lookup("RemoteObject");
        dtm = remoteObject.getComments(ID);
        int size = dtm.getRowCount();
        String main = new String();
        col.setText(String.valueOf(size));
        if (size > 0) {
            main = "\n";
        }
        if (size > 0) {
            for (int i = 0; i < size; i++) {
               
                main += (" " + dtm.getValueAt(i, 1).toString() + "\n\n");
            }
            comments.setText(main);
        } else {
            comments.setText("Нет комментариев");
        }
    }

