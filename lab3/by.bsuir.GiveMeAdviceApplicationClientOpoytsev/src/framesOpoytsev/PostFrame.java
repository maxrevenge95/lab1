    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Comment c = new Comment();
        try {
            if (!comment.getText().equals("")) {
                Registry registry = LocateRegistry.getRegistry("localhost", 1112);
                MyRemote remoteObject = (MyRemote) registry
                        .lookup("RemoteObject");
                c.setCommentText(comment.getText());
                c.setUserID(uID);
                c.setPostID(ID);
                remoteObject.addComment(c);
                JOptionPane.showMessageDialog(this,
                       "Ваш  комментарий добавлен", "ИНформация!",
                        JOptionPane.INFORMATION_MESSAGE);
                comment.setText("");
                refresh();
            } else {
                JOptionPane.showMessageDialog(this,
                        "Введите ваш  комментарий", "Ошибка!",
                        JOptionPane.ERROR_MESSAGE);
            }
        } catch (RemoteException ex) {
            Logger.getLogger(PostFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NotBoundException ex) {
            Logger.getLogger(PostFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(PostFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


